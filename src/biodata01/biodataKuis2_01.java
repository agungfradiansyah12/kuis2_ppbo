    private void DaftarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DaftarButtonActionPerformed
                                              
    // Validasi inputan form
    if (nikText1.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "NIK harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (namaText.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Nama harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (alamatTextArea.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Alamat harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    Object pendidikanItem = pendidikanComboBox.getSelectedItem();
    if (pendidikanItem == null || pendidikanItem.toString().equals("-")) {
        JOptionPane.showMessageDialog(this, "Jurusan harus dipilih!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    ButtonModel genderModel = btnGroupJk.getSelection();
    if (genderModel == null) {
        JOptionPane.showMessageDialog(this, "Gender harus dipilih!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Validasi IPK
    String ipkString = IPKjFormatted.getText().trim();
    if (ipkString.isEmpty()) {
        JOptionPane.showMessageDialog(this, "IPK harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    try {
        double ipk = Double.parseDouble(ipkString);
        if (ipk < 3.01 || ipk > 4.00) {
            JOptionPane.showMessageDialog(this, "IPK harus antara 3.01 dan 4.00", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "IPK tidak valid. Masukkan angka yang benar.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Menampilkan pesan pendaftaran berhasil
    JOptionPane.showMessageDialog(this, "Anda berhasil mendaftar", "Pendaftaran Berhasil", JOptionPane.INFORMATION_MESSAGE);
    
    // Reset form setelah pendaftaran berhasil
    
    nikText1.setText(""); 
    namaText.setText(""); 
    alamatTextArea.setText(""); 
    btnGroupJk.clearSelection();
    TelpText.setText(""); 
    menulisCheckBox.setSelected(false);
    membacaCheckBox.setSelected(false);
    fotografiCheckBox.setSelected(false);
    ftftgllahir.setText(""); 
    pendidikanComboBox.setSelectedIndex(-1); 
    IPKjFormatted.setText("");
    
//    resetForm();
}

