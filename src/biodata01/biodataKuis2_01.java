    private void TelpTextKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TelpTextKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
    // Cek apakah karakter yang dimasukkan bukan angka
    if (!Character.isDigit(c)) {
        evt.consume(); // Abaikan karakter jika bukan angka
    }
    }//GEN-LAST:event_TelpTextKeyTyped

    private void IPKjFormattedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IPKjFormattedActionPerformed
        // TODO add your handling code here:
       String ipkString = IPKjFormatted.getText().trim();

    try {
        if (!ipkString.isEmpty()) {
            double ipk = Double.parseDouble(ipkString);

            if (ipk >= 0.00 && ipk <= 4.00) {
                System.out.println("IPK valid: " + ipk);
            } else {
                JOptionPane.showMessageDialog(this, "IPK tidak valid. Harus antara 0.00 dan 4.00.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
                IPKjFormatted.setText("");
            }
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "IPK tidak valid. Masukkan angka dengan format yang benar.",
                "Error", JOptionPane.ERROR_MESSAGE);
        IPKjFormatted.setText("");
    }
    }//GEN-LAST:event_IPKjFormattedActionPerformed

    private void ftftgllahirFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ftftgllahirFocusLost
        // TODO add your handling code here:
        try {
        String tanggalLahirText = ftftgllahir.getText().trim();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate tanggalLahir = LocalDate.parse(tanggalLahirText, formatter);

        LocalDate sekarang = LocalDate.now();
        int umur = Period.between(tanggalLahir, sekarang).getYears();

        if (umur < 18) {
            JOptionPane.showMessageDialog(null, "Usia tidak memenuhi syarat. Minimal 18 tahun.");
            ftftgllahir.requestFocus(); 
        }
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "Format tanggal tidak valid. Gunakan format yyyy-MM-dd.");
            ftftgllahir.requestFocus(); 
        }
    }//GEN-LAST:event_ftftgllahirFocusLost

